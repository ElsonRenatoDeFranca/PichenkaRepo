DROP TABLE IF EXISTS CUSTOMER;

CREATE TABLE CUSTOMER (  
   CUST_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL,
   CUST_PERSONAL_ID INTEGER NOT NULL,
   CUST_FIRST_NAME VARCHAR(30) NOT NULL, 
   CUST_LAST_NAME VARCHAR(30) NOT NULL,
   CUST_EMAIL VARCHAR(30) NOT NULL,
   PRIMARY KEY(CUST_PERSONAL_ID)
);